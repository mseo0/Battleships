class Coordinate{
  private int xCord;
  private int yCord;

  //creates a new object witht he given coords
  public Coordinate(int x, int y){
    xCord = x;
    yCord = y;
  }

  //getter for the xCord
  public int getXCord(){
    return xCord;
  }


  //getter for the yCord
  public int getYCord(){
    return yCord;
  }
}